# Este script mostrará por pantalla el cociente y el resto de una división entre 2 números
n1 = int(input("Introduce el primer número: "))
m2 = int(input("Introduce el segundo número: "))
c0 = n1//m2
r0 = n1%m2
print(f"El cociente de la división entre {n1} y {m2} es: {c0}")
print(f"El resto de la división entre {n1} y {m2} es: {r0}")