# Calcula el sueldo en función de las horas trabajadas y el coste por cada hora de trabajo
nHoras = int(input("Introduce el número de horas trabajadas: "))
precioHora = int(input("Introduce el coste por hora: "))
print(f"La paga correspondiente es de: {nHoras*precioHora}€")